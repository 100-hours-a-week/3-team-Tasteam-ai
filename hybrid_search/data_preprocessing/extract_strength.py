# 강점 추출
# 1. 전체 음식점 대상 빈도 데이터가 있다. 그걸 음식점 개수로 나눈 평균 값을 가지고 있다
# 2. 각 음식점의 빈도를 센다.
# 3. 각 음식점이 평균보다 높은 값이 있다면, 그걸 명시하라. 예시 ("이 음식점은 판교 전체 음식점 평균 대비 서비스에 대한 칭찬이 20%나 높아요.")


# 1. (이 음식점의 서비스 긍정 언급 수) / 이 음식점의 전체 리뷰 수
# 2. 전체 음식점 평균 서비스 
# 3. (강점 지수 (%)) = (이 음식점 비중 - 전체 평균 비중) / 전체 평균 비중 * 100
# 4. 조정 비중 = (서비스 긍정 언급 수 + a) / (전체 리뷰 수 + b)